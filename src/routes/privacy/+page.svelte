				<p>Plan Your Recovery is required by law to maintain the privacy of your protected health information (PHI), to provide you with notice of our legal duties and privacy practices with respect to your PHI, and to notify you following a breach of your unsecured PHI. We are required to abide by the terms of this notice while it is in effect.</p>
