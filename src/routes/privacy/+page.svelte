			<section>
				<h2>Confidentiality of Substance Use Disorder Records</h2>
				<p>If you are receiving or have received services for substance use disorders, your records may be protected by additional federal rules under 42 CFR Part 2. These rules provide greater confidentiality protections than standard HIPAA provisions.</p>
				<p>Under 42 CFR Part 2, we generally may not disclose that you attend our program, or any information identifying you as a person with a substance use disorder, without your written consent — except in very limited circumstances such as a medical emergency, a court order, or mandatory reporting of child abuse or neglect.</p>
				<p>Violations of 42 CFR Part 2 may be reported to the United States Attorney in the district where the violation occurred.</p>
			</section>

			<section>
				<h2>Uses and Disclosures</h2>
				<p>We use and disclose health information about you for treatment, payment, and healthcare operations. For example:</p>
				<p><strong>Treatment.</strong> We may use or disclose your PHI to a physician or other healthcare provider providing treatment to you.</p>
				<p><strong>Payment.</strong> We may use and disclose your PHI to obtain payment for services we provide to you.</p>
				<p><strong>Healthcare Operations.</strong> We may use and disclose your PHI in connection with our healthcare operations, including quality assessment and improvement activities, licensing, and conducting or arranging for other business activities.</p>
				<p>We may also use or disclose your PHI in the following situations without your authorization: as required by law; for public health activities; for health oversight activities; for judicial and administrative proceedings; for law enforcement; to avert a serious threat to health or safety; for workers' compensation; and for specialized government functions including military and veterans' activities.</p>
			</section>

			<section>
				<h2>Authorization to Use or Disclose PHI</h2>
				<p>For uses and disclosures beyond treatment, payment, and healthcare operations, we are required to have your written authorization unless the use or disclosure is otherwise permitted or required by law. We will also obtain your written authorization before using your PHI for marketing purposes and before selling your PHI. Your authorization may be revoked in writing at any time. Upon receipt of the written revocation, we will stop using or disclosing your PHI, except to the extent that we have already taken action in reliance on your authorization.</p>
			</section>

			<section>
				<h2>Your Rights as a Patient</h2>

				<h3>Right to Notice</h3>
				<p>You have the right to receive a paper copy of this notice. You may ask us to give you a copy of this notice at any time. Even if you have agreed to receive this notice electronically, you are still entitled to a paper copy.</p>

				<h3>Right of Access</h3>
				<p>You have the right to look at or get an electronic or paper copy of your medical record and other health information we have about you. Ask us how to do this. We will provide a copy or a summary of your health information, usually within 30 days of your request. We may charge a reasonable, cost-based fee.</p>

				<h3>Right to Amend</h3>
				<p>You have the right to request that we correct health information about you that you think is incorrect or incomplete. Ask us how to do this. We may say "no" to your request, but we will tell you why in writing within 60 days.</p>

				<h3>Right to an Accounting of Disclosures</h3>
				<p>You have the right to request a list of the times we've shared your health information for six years prior to the date you ask, who we shared it with, and why. We will include all the disclosures except for those about treatment, payment, and health care operations, and certain other disclosures (such as any you asked us to make). We'll provide one accounting a year for free but will charge a reasonable, cost-based fee if you ask for another one within 12 months.</p>

				<h3>Right to Request Restrictions</h3>
				<p>You have the right to request that we place additional restrictions on our use or disclosure of your PHI. We are not required to agree to these additional restrictions, but if we do, we will abide by our agreement (except in an emergency).</p>

				<h3>Out-of-Pocket Payments</h3>
				<p>If you paid out-of-pocket in full for a specific item or service, you have the right to ask that your PHI with respect to that item or service not be disclosed to a health plan for purposes of payment or health care operations, and we will honor that request.</p>

				<h3>Right to Confidential Communications</h3>
				<p>You have the right to request that we communicate with you about your health information by alternative means or to alternative locations. For example, you can ask that we only contact you at work or by mail. We will accommodate all reasonable requests. Your request must specify how or where you wish to be contacted.</p>

				<h3>Right to Notification of a Breach</h3>
				<p>You will receive notifications of breaches of your unsecured PHI as required by law.</p>

				<h3>Right to Voice Concerns</h3>
				<p>You have the right to complain to us or to the Secretary of Health and Human Services if you believe your privacy rights have been violated. To file a complaint with us, contact us using the information below. All complaints must be submitted in writing. We will not retaliate against you for filing a complaint.</p>
			</section>

